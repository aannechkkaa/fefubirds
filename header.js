function showContent(){
  var up = document.getElementById('uppermobile');
  var hwrap = document.getElementById('hwrapper');
  if (up.style.display = "none") {
      up.style.display = "block";
    }
    if (hwrap.style.display = "none") {
        hwrap.style.display = "block";
      }
}
function hidecontent(){
  var upclose = document.getElementById('uppermobile');
  var hwrapclose = document.getElementById('hwrapper');
  if (upclose.style.display = "block") {
      upclose.style.display = "none";
    }
    if (hwrapclose.style.display = "block") {
        hwrapclose.style.display = "none";
      }
}
